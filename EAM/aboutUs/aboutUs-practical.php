<h1><font color="#D9853B">Passport Visa, Money and Weather</font></h1>

Citizens of the European Union countries as well as of Switzerland, Iceland, Lichtenstein and Norway do not require an entry visa may enter Cyprus either with a Passport or their national identity card provided there is a photo.
<br>
<br>
The full list other countries whose citizens do not need an entry visa, other than a valid passport, as well as of those that entry visas are required, as well the application for a visa form are found in the Ministry of Foreign Affairs of the Republic of Cyprus web page: www.mfa.gov.cy.
<br>
<br>
The currency of the Republic of Cyprus is the Euro since 1st of Jan 2008 which is sub-divided into 100cents. For information concerning exchange of currency, please visit the following webpage: http://www.centralbank.gov.cy go to Tools and activate the currency exchange. Credit cards (Visa, MasterCard, American Express, etc) are widely accepted.
<br>
<br>
Cyprus enjoys an intense Mediterranean climate. The average temperature in October-November during the day is around 25C degrees and drops to 13C degrees during the evening (http://www.euronews.com/weather/europe/cyprus/)
