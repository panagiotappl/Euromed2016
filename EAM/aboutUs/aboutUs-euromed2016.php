      <h1><font color="#D9853B">What are the EuroMed Conferences </font></h1>
The European Mediterranean Conference (EuroMed) is a biannual event organised by Digital Heritage Research Lab
 of Cyprus University of Technology and has become a regular worldwide milestone on Cultural Heritage interdisciplinary research.
<br>
<br>

 Joint European and international research produce a scientific background and support for changing our behaviour on Protecting,
 Preserving and Presenting global Cultural Heritage.

 EuroMed Conference enhances this aspect by bringing together hundreds of expertise from academia,
 research and industrial sectors, from more than 60 countries.
 <br>
 <br>
  EuroMed Conference is focused on conservation, massive digitalisation, modelling, archiving,
  visualisation and preservation of Tangible and Intangible Cultural Heritage. The event is also focusing on documentation and modelling of the knowledge (story).


<h1><font color="#D9853B">  Who can participate</font></h1>
  Several Organisations and EU Projects can join EuroMed in order to create an optimal environment for the discussion, explanation of new technologies, exchange of ideas and in general to allow the transfer of knowledge between a maximum number of professionals and participants.
  <br>There are two main thematic fields on EuroMed Conference:
<br><br>  ● Digital Cultural Heritage.
<br>  ● Protection, Restoration and Preservation of Tangible and Intangible Cultural Heritage.
<br><br>  At EuroMed Conference, participants have the opportunity to experience original and innovative presentations, demonstrations, panel discussions, exhibitions and workshops.


<h1><font color="#D9853B"> Issues addressed at EuroMed 2016 </font></h1>

The Preservation and Protection of Cultural Heritage is more crucial than ever before. All over the world Cultural Heritage is endangered, vandalised, destroyed or looted. Through special thematic focused sessions the EuroMed 2016 Conference will address these issues, from the aspect of legislation, through cutting edge technologies, innovation, humanities, ontologies and digitalisation, covering content from archeological sites, artefacts, monuments, libraries, archives, museums, etc.

