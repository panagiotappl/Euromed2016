 <h1><font color="#D9853B"> The Digital Heritage Research Lab </font></h1>

 The Digital Heritage Research Lab is underthe Department of Electrical Engineering and Information Technology of the Cyprus University of Technology. The lab was established in 2013 and it is the first, and so far the only one, on the island of Cyprus that specializes in this field.
 <br> <br>Our aim is to explore issues such as digitization, archiving and the promotion of cultural heritage, tangible and intangible remains of our cultural past. Moreover, the lab focuses on how the latest technological developments can deliver this legacy to users. We collaborate on a national level with all relevant private and public sectors. At a European level the lab collaborates with a network of at least 150 leading partners from the academic, research and industrial sectors working towards the development of new tools and applications.
 <br> <br>The research focuses on four thematic areas: recording, access, management and preservation.
 <br> <br>The Laboratory of Digital Cultural Heritage is a prototype of cooperation and has rapidly achieved world-spectrum collaboration research programs and has created a unique and wide research network with great potentials for future activities. The input of the lab is significant, since it promotes and supports an advanced scientific basis.


